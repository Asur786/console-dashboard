# MAGIC | Full table path | `workspace.preferences.user_dashboard_views` |
# MAGIC | Format | Delta |
# MAGIC | Primary key (logical) | `user_id` + `view_id` |
# MAGIC | Default constraint | At most one `is_default = TRUE` per `user_id` (enforced in service layer) |
